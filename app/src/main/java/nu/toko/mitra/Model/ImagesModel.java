package nu.toko.mitra.Model;

public class ImagesModel {

    private String id_gambar;
    private String id_produk;
    private String url_gambar;

    public ImagesModel() {

    }

    public String getId_gambar() {
        return id_gambar;
    }

    public void setId_gambar(String id_gambar) {
        this.id_gambar = id_gambar;
    }

    public String getId_produk() {
        return id_produk;
    }

    public void setId_produk(String id_produk) {
        this.id_produk = id_produk;
    }

    public String getUrl_gambar() {
        return url_gambar;
    }

    public void setUrl_gambar(String url_gambar) {
        this.url_gambar = url_gambar;
    }
}
