package nu.toko.mitra.Model;

public class KotaModel {

    private String provinsi_id;
    private String kota_id;
    private String nama_kota;

    public KotaModel() {
    }

    public String getProvinsi_id() {
        return provinsi_id;
    }

    public void setProvinsi_id(String provinsi_id) {
        this.provinsi_id = provinsi_id;
    }

    public String getKota_id() {
        return kota_id;
    }

    public void setKota_id(String kota_id) {
        this.kota_id = kota_id;
    }

    public String getNama_kota() {
        return nama_kota;
    }

    public void setNama_kota(String nama_kota) {
        this.nama_kota = nama_kota;
    }
}
